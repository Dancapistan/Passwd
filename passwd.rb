#!/usr/bin/env ruby

class PasswordGenerator
  def initialize
    @words = DATA.readlines
  end
  
  # Returns a password composed of a collection 
  # of random words
  def generate(num_words)
    words = Array.new
    (1..num_words).to_a.each do
      words.push random_word
    end
    words.join ' '
  end
  
  # Returns a random word from the word list
  def random_word
    @words.fetch(rand(@words.length)).chomp
  end
  
end

unless __FILE__ == File.expand_path(__FILE__)
  count = (ARGV[0] || 4).to_i
  pass = PasswordGenerator.new
  puts pass.generate(count)
end

